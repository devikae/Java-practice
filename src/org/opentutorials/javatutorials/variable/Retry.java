package org.opentutorials.javatutorials.variable;

public class Retry {

	public static void main (string[] args) { 
		System.out.println("Hello ikae");           // 문자열 출력
		System.out.println('A');                    // 문자 출력
		System.out.println("Hi"+" every one");      // 문자열 + 문자열
		System.out.println("3+4=");            
		System.out.println(3+4);                    // 연산자 사용 
		System.out.println("Hello \n ikae");        // \n 줄바꿈 
		System.out.println("ikae said \"YAHO\"");   // \로 큰 따옴표 이스케이프
		
		
		int a, b;                                   //정수 타입 변수 선언 
		a = 100; b = 70;                            //변수 값 할당 
		System.out.println("ikae는 곧" + (a-b));     //계산, 출력
		
		double x, y;                                //실수 타입 변수 선언
		x = 1.3;                                    //각각 값 할당
		y = 1.7;
		System.out.println("1.3 + 1.7 = " + (x+y)); //계산, 출력

		String SE = "ikae";                         //문자열 변수 선언
		System.out.println(SE+" said so cold");     //문자열 출력
		                                               
		String i = "ikae said ";
		System.out.println(i +"kk");
		System.out.println(i +"kkkkkk");
		System.out.println(i +"ㅠㅠ");
		System.out.println(i +"ㅗㅗ");
		
		/**
		 * dd
		 * dd
		 * dd
		 * dd
		 * dd
		 */

	}

}
